char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*pst;

	pst = 0;
	i = 0;
	while (s[i] != 0)
	{
		if (c == s[i])
		{
			pst = (char *) &s[i];
			return (pst);
		}
		i++;
	}
	return (pst);
}
